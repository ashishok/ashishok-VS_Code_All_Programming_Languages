public class Variables {
    public static void main(String[] args) {
        int x = 123; // initialization
        float y = 1.3f;
        double z = 1.310; // double z = 1.310d; OR double z = 1.310D;
        long ln = 123456789712L; // long ln = 123456789712l;
        char ch = '@';
        String name = "ashish";

        System.out.println("My Number is : " + x);
        System.out.println("My Second Number is : " + y);
        System.out.println("My Third Number is : " + z);
        System.out.println("My Fourth Number is : " + ln);
        System.out.println("My First character is : " + ch);
        System.out.println("My Name is : " + name);
    }
}
