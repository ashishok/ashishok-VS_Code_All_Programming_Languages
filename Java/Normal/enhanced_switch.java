public class enhanced_switch {
    public static void main(String[] args) {
        String var = "ashish";

        switch (var) 
        {
        case "shubham" -> {System.out.println("hello Shubham");}
        case "suraj" -> {System.out.println("Hello Suraj");}
        case "ashish" -> {System.out.println("hello ashish");}
        default -> {System.out.println("This is default Key");}
        }
    }

}
